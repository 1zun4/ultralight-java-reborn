package net.janrupf.ujr.api.logger;

/**
 * Log levels, used with {@link UltralightLogger#logMessage(UltralightLogLevel, String)}.
 */
public enum UltralightLogLevel {
    /**
     * Error messages.
     */
    ERROR,

    /**
     * Warning messages.
     */
    WARNING,

    /**
     * Informational messages.
     */
    INFO
}
