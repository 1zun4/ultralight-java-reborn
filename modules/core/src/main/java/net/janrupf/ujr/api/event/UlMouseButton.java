package net.janrupf.ujr.api.event;

/**
 * The various mouse button types.
 */
public enum UlMouseButton {
    /**
     * No button was involved in an event.
     */
    NONE,

    /**
     * The left mouse button was involved in an event.
     */
    LEFT,

    /**
     * The middle mouse button was involved in an event.
     */
    MIDDLE,

    /**
     * The right mouse button was involved in an event.
     */
    RIGHT,
}
