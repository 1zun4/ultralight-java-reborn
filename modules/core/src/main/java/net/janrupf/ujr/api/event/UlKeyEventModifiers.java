package net.janrupf.ujr.api.event;

/**
 * An enumeration of the different keyboard modifiers.
 */
public enum UlKeyEventModifiers {
    /**
     * The alt key is pressed.
     */
    ALT,

    /**
     * The control key is pressed.
     */
    CTRL,

    /**
     * The meta key (also known as "Windows key") is pressed.
     */
    META,

    /**
     * The shift key is pressed.
     */
    SHIFT
}
