package net.janrupf.ujr.api.javascript;

/**
 * All possible JavaScript typed array types.
 */
public enum JSTypedArrayType {
    /**
     * Int8Array
     */
    INT8_ARRAY,

    /**
     * Uint8Array
     */
    UINT8_ARRAY,

    /**
     * Uint8ClampedArray
     */
    UINT8_CLAMPED_ARRAY,

    /**
     * Int16Array
     */
    INT16_ARRAY,

    /**
     * Uint16Array
     */
    UINT16_ARRAY,

    /**
     * Int32Array
     */
    INT32_ARRAY,

    /**
     * Uint32Array
     */
    UINT32_ARRAY,

    /**
     * Float32Array
     */
    FLOAT32_ARRAY,

    /**
     * Float64Array
     */
    FLOAT64_ARRAY,

    /**
     * Buffer
     */
    ARRAY_BUFFER,

    /**
     * Not a typed array
     */
    NONE
}
